let totalChanges = 0;

for (const [file, changes] of Object.entries(rewrites)) {
  let html = fs.readFileSync(file, 'utf8');
  let fileChanges = 0;

  for (const { old: oldText, new: newText } of changes) {
    if (html.includes(oldText)) {
      html = html.replace(oldText, newText);
      fileChanges++;
    } else {
      // Try trimmed match
      const trimOld = oldText.trim();
      if (html.includes(trimOld)) {
        html = html.replace(trimOld, newText);
        fileChanges++;
      }
    }
  }

  fs.writeFileSync(file, html);
  totalChanges += fileChanges;
  console.log(`✓ ${file}: ${fileChanges}/${changes.length} changes`);
}

console.log(`\nTotal: ${totalChanges} changes applied`);
